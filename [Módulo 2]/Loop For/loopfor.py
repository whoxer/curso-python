# Controle de Fluxo - Loop For

# Lista criada com nomes de revolucionários na história da humanidade
nomes_de_revolucionarios =  [
    'Malcom X',
    'Karl Marx',
    'Amaro Félix',
    'Angela Davis',
    'Olga Benário',
    'Alexandra Elbakyan',
    'Jorge Amado'
]

print("Nomes de revolucionários na história da Humanidade: \n")


# O loop for busca pelo nome dentro da lista que criamos
for nome in nomes_de_revolucionarios:
    # Após isso o comando print() exibe nome por nome da lista
    print(nome, '\n')