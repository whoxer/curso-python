# Operadores


# Operadores Aritméticos

# Exemplo de soma
soma = 5 + 3
print("Soma:", soma)

# Exemplo de subtração
subtracao = 10 - 4
print("Subtração:", subtracao)

# Exemplo de multiplicação
multiplicacao = 6 * 2
print("Multiplicação:", multiplicacao) 

# Exemplo de divisão
divisao = 20 / 4
print("Divisão:", divisao)

# Operadores de comparação com variaveis para numeros boleanos (verdadeiro ou falso)

# Exemplo de igualdade
resultado_igualdade = (5 == 5)
print("5 é igual a 5?", resultado_igualdade)  # Saída: True

# Exemplo de desigualdade
resultado_desigualdade = (10 != 5)
print("10 é diferente de 5?", resultado_desigualdade)  # Saída: True

# Exemplo de maior que
resultado_maior_que = (8 > 5)
print("8 é maior que 5?", resultado_maior_que)  # Saída: True

# Exemplo de menor que
resultado_menor_que = (3 < 7)
print("3 é menor que 7?", resultado_menor_que)  # Saída: True

# Exemplo de maior ou igual a
resultado_maior_igual = (10 >= 10)
print("10 é maior ou igual a 10?", resultado_maior_igual)  # Saída: True

# Exemplo de menor ou igual a
resultado_menor_igual = (3 <= 3)
print("3 é menor ou igual a 3?", resultado_menor_igual)  # Saída: True






